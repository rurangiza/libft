#include "libft.h"

void	ft_putnbr_fd(int nb, int fd)
{
	char	ch;

	if (nb > 9)
	{
		ft_putnbr_fd((nb / 10), fd);
		ft_putnbr_fd((nb % 10), fd);
	}
	else if (nb == -2147483648)
	{
		ft_putstr_fd("-2147483648", fd);
	}
	else if (nb < 0)
	{
		ft_putchar_fd('-', fd);
		nb = -nb;
		ft_putnbr_fd(nb, fd);
	}
	else if (nb >= 0 && nb < 10)
	{
		ch = nb + '0';
		ft_putchar_fd(ch, fd);
	}
}

